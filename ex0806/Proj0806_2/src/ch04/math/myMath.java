package ch04.math;

public class myMath {

	public static void main(String[] args) {

		//		Math.max(x, y); 최대값
		System.out.println(Math.max(3, 9));

		//		Math.min(x, y); 최소값
		System.out.println(Math.min(1, 9));

		//		Math.sqrt(x);   제곱근
		System.out.println(Math.sqrt(9));

		//		Math.abs(x);    (부호가 제거된)절대값
		System.out.println(Math.abs(-3.33));

		//		Math.random();  랜덤값
		System.out.println(Math.random());

	}

}
